package com.sunmnet.bigdata.web.model.entity.student;

public class WholePortrait {
	/** 学院代码 **/
	private String collegeCode;
	/** 专业代码 **/
	private String majorCode;
	/** 书院名称 **/
	private String academyName;
	/** 班级 **/
	private String className;
	/** 民族 **/
	private String nation;
	/** 性别 **/
	private String gender;
	/** 生源地 **/
	private String nativePlace;
	/** 培养层次 **/
	private String trainingLevel;
	/** 学年 **/
	private String schoolYear;
	/** 开始时间 **/
	private String startDate;
	/** 结束时间 **/
	private String endDate;
	/** 成绩指数 **/
	private String scoreIndex;
	/** 三餐指数 **/
	private String eatIndex;

	public String getCollegeCode() {
		return collegeCode;
	}

	public void setCollegeCode(String collegeCode) {
		this.collegeCode = collegeCode;
	}

	public String getMajorCode() {
		return majorCode;
	}

	public void setMajorCode(String majorCode) {
		this.majorCode = majorCode;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getNation() {
		return nation;
	}

	public void setNation(String nation) {
		this.nation = nation;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getNativePlace() {
		return nativePlace;
	}

	public void setNativePlace(String nativePlace) {
		this.nativePlace = nativePlace;
	}

	public String getSchoolYear() {
		return schoolYear;
	}

	public void setSchoolYear(String schoolYear) {
		this.schoolYear = schoolYear;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}

	public String getTrainingLevel() {
		return trainingLevel;
	}

	public void setTrainingLevel(String trainingLevel) {
		this.trainingLevel = trainingLevel;
	}

	public String getAcademyName() {
		return academyName;
	}

	public void setAcademyName(String academyName) {
		this.academyName = academyName;
	}

	public String getScoreIndex() {
		return scoreIndex;
	}

	public void setScoreIndex(String scoreIndex) {
		this.scoreIndex = scoreIndex;
	}

	public String getEatIndex() {
		return eatIndex;
	}

	public void setEatIndex(String eatIndex) {
		this.eatIndex = eatIndex;
	}
}
