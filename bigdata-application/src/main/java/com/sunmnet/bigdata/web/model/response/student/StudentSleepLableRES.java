public class StudentSleepLableRES {

    private String sleepLabel;
    private String getupLabel;
    private String sleepTimeLabel;
    private String personalityLabel;

    public String getSleepLabel() {
        return sleepLabel;
    }

    public void setSleepLabel(String sleepLabel) {
        this.sleepLabel = sleepLabel;
    }

    public String getGetupLabel() {
        return getupLabel;
    }

    public void setGetupLabel(String getupLabel) {
        this.getupLabel = getupLabel;
    }

    public String getSleepTimeLabel() {
        return sleepTimeLabel;
    }

    public void setSleepTimeLabel(String sleepTimeLabel) {
        this.sleepTimeLabel = sleepTimeLabel;
    }

    public String getPersonalityLabel() {
        return personalityLabel;
    }

    public void setPersonalityLabel(String personalityLabel) {
        this.personalityLabel = personalityLabel;
    }
}
